#Вариант 22. Даны списки А и В одинакового размера N. Поменять местами их содержимое
#и вывести вначале элементы преобразованного списка А, а затем — элементы преобразованного списка B.

import random
N = int(input('Введите размер списков: '))
A = []
for i in range(N):
    A.append(random.randint(1, 100))
B = []
for i in range(N):
    B.append(random.randint(1, 100))
for i in range(N):
    A[i], B[i] = B[i], A[i]
print('Преобразованный список A:', A)
print('Преобразованный список B:', B)