#Вариант 22. Дано целое число, большее 999. Используя одну операцию деления
#и одну операцию взятия остатка от деления, найти цифру, соответствующую
#разряду сотен в записи этого числа.

try:
    num = int (input ("Введите число большее 999: "))
    if num <= 999:
        print ("Ошибка: число должно быть больше 999")
    else:
        hund = (num // 100) % 10
        print (hund)
except ValueError:
    print ("Ошибка. Попробуй ввести число.")